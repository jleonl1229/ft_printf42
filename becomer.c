#include "ft_printf.h"

int	ft_getchar(char o)
{
	write(1, &o, 1);
	return (1);
}

int	ft_getstr(char *o)
{
	int	i;

	i = 0;
	while (o[i] != '\0')
	{
		write(1, &o[i], 1);
		i++;
	}
	return (i);
}


